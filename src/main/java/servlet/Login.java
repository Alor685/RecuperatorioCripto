    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
   @Override
protected void doPost(HttpServletRequest request, HttpServletResponse response)
        throws ServletException, IOException {
    
    int dni = Integer.parseInt(request.getParameter("dni"));
    String password = request.getParameter("password");

    // Construir objeto Medico con los datos del formulario
       Medico login = new Medico();
    login.setNdniMedi(dni);
    login.setPassMedi(password);

       MedicoJpaController dao = new MedicoJpaController();
    Medico medico = dao.validar(login);

    response.setContentType("application/json");
    PrintWriter out = response.getWriter();
    
    if (medico != null) {
        request.getSession().setAttribute("usuario", medico);
        out.print("{\"success\": true}");
    } else {
        out.print("{\"success\": false}");
    }
}


